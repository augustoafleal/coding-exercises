package streams;

public class Produto {

	final String nome;
	final double preco;
	final boolean desconto;
	final boolean freteGratis;
	
	Produto(String nome, double preco, boolean desconto, boolean freteGratis) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.freteGratis = freteGratis;		
	}
	
	
	
}
